    private void jBconnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconnActionPerformed
        // TODO add your handling code here:
        String mail = jtfMail.getText();
        String pass = jtfPass.getText();
        String nom =  "";
        String prenom = "";
        String droit = "";
        boolean exist = false;
        
        try {
             exist = selectBDD.isuservalid(mail,pass);
        } catch (SQLException ex) {
            Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(exist == true)
        {
            dispose();
            Fenetre_principale MainWindow = new Fenetre_principale();
            MainWindow.setVisible(true);
            
            try {
                droit = selectBDD.checkright(mail);
            } catch (SQLException ex) {
                Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
            }
        
            if(droit.equals("USER")){
                Utilisateurs CurrentU = new Utilisateurs(mail);
                try {
                    nom = CurrentU.getNom();
                } catch (SQLException ex) {
                    Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                }
        
                try {
                    prenom = CurrentU.getPrenom();
                } catch (SQLException ex) {
                    Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                }
        
                try {
                    CurrentU.setPresence(nom, prenom,1);
                } catch (SQLException ex) {
                    Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else{
                if(droit.equals("CHEF_PROJET")){
                    Chef_Projet CurentCP = new Chef_Projet(mail);
                    try {
                        nom = CurentCP.getNom();
                    } catch (SQLException ex) {
                        Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                    }
        
                    try {
                        prenom = CurentCP.getPrenom();
                    } catch (SQLException ex) {
                        Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                    }
        
                    try {
                        CurentCP.setPresence(nom, prenom,1);
                    } catch (SQLException ex) {
                        Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else {
                    if(droit.equals("ADMIN")){
                        Admin CurrentA = new Admin(mail);
                        try {
                            nom = CurrentA.getNom();
                        } catch (SQLException ex) {
                            Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    
                        try {
                            prenom = CurrentA.getPrenom();
                        } catch (SQLException ex) {
                            Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    
                        try {
                            CurrentA.setPresence(nom, prenom ,1);
                        } catch (SQLException ex) {
                            Logger.getLogger(Acceuil.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
        }    
    }//GEN-LAST:event_jBconnActionPerformed

